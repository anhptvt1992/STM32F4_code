char data_recode[100];
uint16_t len;

L3GD20_DATA_RECORD new_data_recode;
L3GD20_DATA_RECORD old_data_recode;

int main(void)
{

  memset(&new_data_recode, 0, sizeof(L3GD20_DATA_RECORD));
  memset(&old_data_recode, 0, sizeof(L3GD20_DATA_RECORD));

  HAL_Init();

  SystemClock_Config();

  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
  set_uart(&huart2);
  L3GD20_Set_SPI(&hspi1);
  if(L3GD20_ReadID() == I_AM_L3GD20)
  {
    sprintf(data_pc, "Hello L3GD20 \r\n");
    len = strlen(data_pc);
    HAL_UART_Transmit_IT(&huart2, (uint8_t *)data_pc, len);
  }
  else
  {
    sprintf(data_pc, "Cannot read L3GD20 \r\n");
    len = strlen(data_pc);
    HAL_UART_Transmit_IT(&huart2, (uint8_t *)data_pc, len);
  }

  L3GD20_Init(0x000F);
	HAL_Delay(1000);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
		
    L3GD20_ReadXYZAngRate(&new_data_recode);
    if(new_data_recode.X_data != old_data_recode.X_data)
    {
			memset(data_pc, 0, 100*sizeof(char));
			len = 0;
      sprintf(data_recode, "X %f\n",new_data_recode.X_data);
      len = strlen(data_recode);
      HAL_UART_Transmit_IT(&huart2, (uint8_t *)data_recode, len);
      old_data_recode.X_data =new_data_recode.X_data;
			HAL_Delay(100);
    }
    if(new_data_recode.Y_data != old_data_recode.Y_data)
    {
			memset(data_pc, 0, 100*sizeof(char));
			len = 0;
      sprintf(data_recode, "Y %f\n",new_data_recode.Y_data);
      len = strlen(data_recode);
      HAL_UART_Transmit_IT(&huart2, (uint8_t *)data_recode, len);
      old_data_recode.Y_data =  new_data_recode.Y_data ;
			HAL_Delay(100);
    }
    if(new_data_recode.Z_data != old_data_recode.Z_data)
    {
			memset(data_pc, 0, 100*sizeof(char));
			len = 0;
      sprintf(data_recode, "Z %f\n",new_data_recode.Z_data);
      len = strlen(data_recode);
      HAL_UART_Transmit_IT(&huart2, (uint8_t *)data_recode, len);
      old_data_recode.Z_data = new_data_recode.Z_data;
			HAL_Delay(100);
    }
    // HAL_Delay(500);
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

